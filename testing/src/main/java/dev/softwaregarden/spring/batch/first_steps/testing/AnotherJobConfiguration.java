package dev.softwaregarden.spring.batch.first_steps.testing;

import org.springframework.batch.core.Job;
import org.springframework.batch.core.configuration.annotation.EnableBatchProcessing;
import org.springframework.batch.core.configuration.annotation.JobBuilderFactory;
import org.springframework.batch.core.configuration.annotation.StepBuilderFactory;
import org.springframework.batch.core.step.tasklet.TaskletStep;
import org.springframework.batch.repeat.RepeatStatus;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@EnableBatchProcessing
@Configuration
public class AnotherJobConfiguration {

    private final JobBuilderFactory jobBuilderFactory;
    private final StepBuilderFactory stepBuilderFactory;

    public AnotherJobConfiguration(JobBuilderFactory jobBuilderFactory, StepBuilderFactory stepBuilderFactory) {
        this.jobBuilderFactory = jobBuilderFactory;
        this.stepBuilderFactory = stepBuilderFactory;
    }

    @Bean
    public Job anotherJob() {
        return jobBuilderFactory.get("anotherJob")
            .start(anotherInfoStep())
            .build();
    }

    @Bean
    public TaskletStep anotherInfoStep() {
        return stepBuilderFactory.get("infoStep")
            .tasklet((contribution, chunkContext) -> {
                System.out.println("another day, another job");
                return RepeatStatus.FINISHED;
            }).build();
    }


}
